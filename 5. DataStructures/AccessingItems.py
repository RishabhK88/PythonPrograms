letters = ["a", "b", "c", "d"]
print(letters[0])
print(letters[-1])
print(letters)
letters[0] = "A"
print(letters)
print(letters[2:5])
# All above operations are same as operations on strings
print(letters[::2])
# The third parameter(after second semicolon) represents steps to skip while printing
# the lists
print(letters[::-1])
# Negative step signifies string should be operated in reverse order
# All above operations can also be done with strings
