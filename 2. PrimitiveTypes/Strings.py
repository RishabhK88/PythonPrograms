course = "Python Programming"
message = """Hi
This is me
3 quotes are used for multiple line strings"""
print(message)
print(len(course))
# len() is used to get length of string
print(course[0])
print(course[-1])
# negative index is used to access characters from string
print(course[0:2])
# above is used to print from character at first index to second index - 1
# for example above is used to print characcters from index 0 to 2-1
print(course[3:])
# end index by default is the one at which string ends
print(course[:6])
# initial index by default is 0
print(course[:])
