print(10 > 3)
print(10 >= 3)
print(10 < 20)
print(10 <= 20)
print(10 == 10)
print(10 == "10")
# it will be false in above case because data types are different
print(10 != 20)
# Comparison operator can also be used for strings
print("bag" > "apple")
# it will be true because when sorted alphabetically, apple comes after bag
print("bag" == "BAG")
# it will be false because the ASCII values of uppercase letters is higher than
# that of lower case letters and so ASCII value of 'b' and 'B' is not equal
print(ord("b"))
print(ord("B"))
# ord is used to find ASCII values of characters
