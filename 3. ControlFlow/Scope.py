# Scope of a variable depends on where it is declared. if a variable is defined in a
# function, it can be accessed only in the function and do not exist outside of
# that function. so a varibale with same name can be declared in different
# functions. these are called local variables. global variables are declared outside any
# function and they are accessible throughout the program. so they stay in memory as
# long as program runs. use of global variables should be avoided.
# if a global variable is to be called or changed in functions, they can be called
# by using the keyword global before variable name.
